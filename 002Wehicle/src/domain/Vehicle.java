package domain;

public class Vehicle {
	
	protected int wheelsNumber;
	protected int maximTolerableMass;
	protected int maximReachableVelocity;
	
	
	public int getWheelsNumber() {
		return wheelsNumber;
	}
	public void setWheelsNumber(int wheelsNumber) {
		this.wheelsNumber = wheelsNumber;
	}
	public int getMaximTolerableMass() {
		return maximTolerableMass;
	}
	public void setMaximTolerableMass(int maximTolerableMass) {
		this.maximTolerableMass = maximTolerableMass;
	}
	public int getMaximReachableVelocity() {
		return maximReachableVelocity;
	}
	public void setMaximReachableVelocity(int maximReachableVelocity) {
		this.maximReachableVelocity = maximReachableVelocity;
	}
	
	
	
	

}
